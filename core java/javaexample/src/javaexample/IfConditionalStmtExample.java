package javaexample;

public class IfConditionalStmtExample {

	public static void main(String[] args) {
		int count = 1;
		if(count>0) {
			System.out.println("Value of count is positive---"+count);
		}else if(count<0) {
			System.out.println("Value of count is negative---"+count);
		}else {
			System.out.println("Value of count is zero---"+count);
			
		}

	}

}
