package javaarryasexample.stringexample;

public class StringFunctionExample {

	public static void main(String[] args) {
		//              012345678910111213
		String hello = "Hello Everyone";
		
		System.out.println(hello.length());
		System.out.println(hello.charAt(10));
		System.out.println(hello.contains("ll"));
		System.out.println(hello.indexOf("n"));

	}

}
